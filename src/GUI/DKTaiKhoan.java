    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        
            // TODO add your handling code here:
            if (!UserBLL.checkSignup(txtUsername.getText(), txtPassword.getText(), txtName.getText(), txtGmail.getText(), txtPhone.getText())){
                txtUsername.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtPassword.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtName.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtGmail.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtPhone.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                lbNoti.setText("Sign up failed! Please check your information");
                
                lbNoti.setVisible(true);
                if (!UserBLL.checkPhone(txtPhone.getText())){
                    txtPhone.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
                }
                if (!UserBLL.checkGmail(txtGmail.getText()))
                {
                    txtGmail.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
                }
                if (!UserBLL.checkUserName(txtUsername.getText()))
                {
                    txtUsername.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
                }
                if (!UserBLL.checkPassword(txtPassword.getText()))
                {
                    txtPassword.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
                }
                if (!UserBLL.checkName(txtName.getText()))
                {
                    txtName.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.red));
                }
                   
            }
            else
            {
                txtUsername.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtPassword.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtName.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtGmail.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                txtPhone.setBorder(BorderFactory.createMatteBorder(1, 1, 1, 1, Color.BLACK));
                lbNoti.setText("Sign up was succeeded! Comeback and Sign in");
                lbNoti.setVisible(true);
                UserBLL.getSignup(txtUsername.getText(), txtPassword.getText(), txtName.getText(), txtGmail.getText(), txtPhone.getText());
            }
        
            
    }//GEN-LAST:event_btnConfirmActionPerformed

